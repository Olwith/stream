# Integer example
a = 10
b = -5
c = 0

print(a)  # Output: 10
print(b)  # Output: -5
print(c)  # Output: 0

# Float example
x = 10.5
y = -3.14
z = 0.0

print(x)  # Output: 10.5
print(y)  # Output: -3.14
print(z)  # Output: 0.0

# String example
name = "Alice"
greeting = 'Hello, world!'
empty_string = ""

print(name)         # Output: Alice
print(greeting)     # Output: Hello, world!
print(empty_string) # Output: 

# Boolean example
is_sunny = True
is_raining = False

print(is_sunny)   # Output: True
print(is_raining) # Output: False

# List example
fruits = ["apple", "banana", "cherry"]
numbers = [1, 2, 3, 4, 5]
mixed_list = [1, "apple", 3.5, True]

print(fruits)     # Output: ['apple', 'banana', 'cherry']
print(numbers)    # Output: [1, 2, 3, 4, 5]
print(mixed_list) # Output: [1, 'apple', 3.5, True]

# Tuple example
coordinates = (10.0, 20.0)
mixed_tuple = (1, "apple", 3.5, True)

print(coordinates)  # Output: (10.0, 20.0)
print(mixed_tuple)  # Output: (1, 'apple', 3.5, True)

# Dictionary example
person = {
    "name": "Alice",
    "age": 25,
    "is_student": True
}

print(person)           # Output: {'name': 'Alice', 'age': 25, 'is_student': True}
print(person["name"])   # Output: Alice
print(person["age"])    # Output: 25
print(person["is_student"])  # Output: True

# Set example
unique_numbers = {1, 2, 3, 4, 5}
mixed_set = {1, "apple", 3.5, True, 1}  # Duplicates will be removed

print(unique_numbers)  # Output: {1, 2, 3, 4, 5}
print(mixed_set)       # Output: {1, 3.5, 'apple', True}

# NoneType example
value = None

print(value)  # Output: None
